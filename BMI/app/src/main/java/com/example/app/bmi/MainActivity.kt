@Composable
fun Bmi() {
    var heightInput: String by remember {
        mutableStateOf("")
    }
    var weightInput: String by remember {
        mutableStateOf("")
    }
    val height = heightInput.toFloatOrNull() ?: 0.0f
    val weight = weightInput.toIntOrNull() ?: 0
    val bmi =
        if (weight > 0 && height > 0) weight / (height * height) else 0.0

    
}

